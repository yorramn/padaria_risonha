     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request,$id)
    {
        $user = \App\Models\User::findOrFail($id);
        $role = Role::findByName($request->role_name);
        $userRole = $user->roles->pluck('name')->first();
        if (isset($user) and isset($role)) {
            if ($user->syncRoles([$role])) {
                return redirect('/user')->with("msg","Cargo de ".$user->name." atualizado de ".$userRole." para ".$role->name);
            } else {
                return redirect('/user')->with("msg","Cargo de".$user->name." n√£o foi atualizado");
            }
        } else {
            return redirect('/');
        }
    }

